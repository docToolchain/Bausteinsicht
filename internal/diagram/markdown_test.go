package diagram

import (
	"strings"
	"testing"
)

func TestWrapDiagramsInMarkdown(t *testing.T) {
	diagrams := map[string]string{
		"context": "C4Context\n    title Context View\n    Person(user, \"User\")",
		"container": "C4Container\n    title Container View\n    Container(api, \"API\")",
	}
	viewTitles := map[string]string{
		"context": "System Context",
		"container": "Container Architecture",
	}
	viewKeys := []string{"context", "container"}

	output := WrapDiagramsInMarkdown(viewKeys, diagrams, viewTitles)

	// Check header
	if !strings.Contains(output, "# Architecture Diagrams") {
		t.Fatal("missing header")
	}

	// Check metadata
	if !strings.Contains(output, "Auto-generated by bausteinsicht") {
		t.Fatal("missing metadata")
	}
	if !strings.Contains(output, "Last updated:") {
		t.Fatal("missing timestamp")
	}

	// Check sections
	if !strings.Contains(output, "## System Context") {
		t.Fatal("missing context section title")
	}
	if !strings.Contains(output, "## Container Architecture") {
		t.Fatal("missing container section title")
	}

	// Check Mermaid blocks
	if !strings.Contains(output, "```mermaid") {
		t.Fatal("missing mermaid block opener")
	}
	if !strings.Contains(output, "C4Context") {
		t.Fatal("missing context diagram")
	}
	if !strings.Contains(output, "C4Container") {
		t.Fatal("missing container diagram")
	}
}

