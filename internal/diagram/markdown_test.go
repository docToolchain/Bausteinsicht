func TestWrapDiagramsInMarkdownSingleView(t *testing.T) {
	diagrams := map[string]string{
		"context": "C4Context\n    Person(user, \"User\")",
	}
	viewTitles := map[string]string{
		"context": "Architecture",
	}
	viewKeys := []string{"context"}

	output := WrapDiagramsInMarkdown(viewKeys, diagrams, viewTitles)

	if !strings.Contains(output, "## Architecture") {
		t.Fatal("missing section title")
	}
	if !strings.Contains(output, "C4Context") {
		t.Fatal("missing diagram")
	}
}

func TestWrapDiagramsInMarkdownOrderPreserved(t *testing.T) {
	diagrams := map[string]string{
		"z": "Z",
		"a": "A",
		"m": "M",
	}
	viewTitles := map[string]string{
		"z": "Z View",
		"a": "A View",
		"m": "M View",
	}
	viewKeys := []string{"z", "a", "m"}

	output := WrapDiagramsInMarkdown(viewKeys, diagrams, viewTitles)

	// Check that order is preserved (Z before A before M)
	zPos := strings.Index(output, "## Z View")
	aPos := strings.Index(output, "## A View")
	mPos := strings.Index(output, "## M View")

	if zPos < 0 || aPos < 0 || mPos < 0 {
		t.Fatal("missing sections")
	}
	if !(zPos < aPos && aPos < mPos) {
		t.Fatal("view order not preserved")
	}
}
